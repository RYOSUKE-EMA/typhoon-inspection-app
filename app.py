@app.route("/resources/<category>/upload", methods=["POST"])
def upload_resource(category):
    get_inspection_type(category)
    f = request.form
    title = f.get("title", "").strip()
    description = f.get("description", "").strip()
    uploaded_by = f.get("uploaded_by", "").strip()
    file = request.files.get("file")

    if title and file and file.filename:
        data = file.read()
        mimetype = file.mimetype or mimetypes.guess_type(file.filename)[0] or "application/octet-stream"
        conn = get_db()
        try:
            db_execute(conn, """
                INSERT INTO resources (title, description, filename, mimetype, data, uploaded_by, uploaded_at, category)
                VALUES (?,?,?,?,?,?,?,?)
            """, (title, description, file.filename, mimetype,
                  psycopg2.Binary(data) if USE_PG else sqlite3.Binary(data),
                  uploaded_by, datetime.now().isoformat(timespec="seconds"), category))
            conn.commit()
        finally:
            conn.close()
    return redirect(url_for("resources_list", category=category))


@app.route("/resources/<int:resource_id>/download")
def download_resource(resource_id):
    conn = get_db()
    try:
        res = fetchone(db_execute(conn, "SELECT * FROM resources WHERE id=?", (resource_id,)))
    finally:
        conn.close()
    if not res:
        abort(404)
    mimetype = res["mimetype"] or mimetypes.guess_type(res["filename"])[0] or "application/octet-stream"
    return send_file(
        BytesIO(to_bytes(res["data"])),
        mimetype=mimetype,
        as_attachment=False,
        download_name=res["filename"],
    )


@app.route("/resources/<int:resource_id>/delete", methods=["POST"])
def delete_resource(resource_id):
    conn = get_db()
    try:
        res = fetchone(db_execute(conn, "SELECT category FROM resources WHERE id=?", (resource_id,)))
        category = res["category"] if res else "typhoon"
        db_execute(conn, "DELETE FROM resources WHERE id=?", (resource_id,))
        conn.commit()
    finally:
        conn.close()
    return redirect(url_for("resources_list", category=category))


